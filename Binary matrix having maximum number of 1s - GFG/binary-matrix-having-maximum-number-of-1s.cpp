class Solution {
public:
    vector<int> findMaxRow(vector<vector<int>> mat, int n) {
        //code here
        int row=0,cnt=0,i=0;
        for(auto it:mat){
            int c=lower_bound(it.begin(),it.end(),1)-it.begin();
            if(cnt<n-c){
                row=i;
                cnt=n-c;
            }
            i++;
        }
        return {row,cnt};
    }
};

